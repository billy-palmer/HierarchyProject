
public class TwoDimensional extends Shape{
	   public TwoDimensional(double length,double width)
	   {
	   super(length,width);
	 
	   this.width=width;
	  this.length=length;
		  
	   }
	  
	   public double getWidth() {return width;}
	   public double getLength() {return length;}
	   
	   
	   
}
